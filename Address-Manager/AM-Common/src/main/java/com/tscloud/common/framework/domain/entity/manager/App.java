package com.tscloud.common.framework.domain.entity.manager;

import com.tscloud.common.framework.domain.TrackableEntity;

import java.io.Serializable;

/**
 * 应用管理
 * Created by shm on 2015/9/9.
 */
public class App extends TrackableEntity implements Serializable {
    private static final long serialVersionUID = 766159126795711222L;

    private String url;     //应用服务地址
    private String image;   //图片
    private String name;    //应用名称
    private String fileName;    //图标名称
    private String code;    //应用编码
    private String remark;    //备注

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
